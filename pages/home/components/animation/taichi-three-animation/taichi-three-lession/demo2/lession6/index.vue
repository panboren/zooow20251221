          for (let y of range(IMAGE_SIZE)) {
            let dx = x - cx
            let dy = y - cy
            let dist = ti.sqrt(dx * dx * 1.0 + dy * dy * 1.0)

            if (dist <= size) {
              pixels[[x * 1.0, y * 1.0]] = [col[0], col[1], col[2], 1.0]
            }
          }
        }
      }
    })


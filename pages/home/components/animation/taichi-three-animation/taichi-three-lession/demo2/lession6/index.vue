        // éšæœºé¢œè‰² - å½©è™¹è‰²
        let hue = ti.random()
        if (hue < 0.33) {
          colors[i] = [
            (1.0 - hue / 0.33) * 1.0,
            (hue / 0.33) * 1.0,
            0.0 * 1.0
          ]
        } else if (hue < 0.66) {
          colors[i] = [
            0.0 * 1.0,
            (1.0 - (hue - 0.33) / 0.33) * 1.0,
            ((hue - 0.33) / 0.33) * 1.0
          ]
        } else {
